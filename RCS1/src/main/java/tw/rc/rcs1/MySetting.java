package tw.rc.rcs1;

public class MySetting {
	private String title;
	private String bigLottery;
	private String powerLottery;
	private String todayLottery;
	public String getTitle() {
		return title;
	}
	public String getBigLottery() {
		return bigLottery;
	}
	public String getPowerLottery() {
		return powerLottery;
	}
	public String getTodayLottery() {
		return todayLottery;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public void setBigLottery(String bigLottery) {
		this.bigLottery = bigLottery;
	}
	public void setPowerLottery(String powerLottery) {
		this.powerLottery = powerLottery;
	}
	public void setTodayLottery(String todayLottery) {
		this.todayLottery = todayLottery;
	}
		
	
	
	
	
}
