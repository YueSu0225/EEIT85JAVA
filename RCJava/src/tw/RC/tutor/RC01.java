package tw.RC.tutor;

public class RC01 {

	public static void main(String[] args) {
		// 整數: byte, short, int, long
		byte var1; // 2^8 => -128 ~ 127
		var1 = 12;
		byte var2 = 123;	// [a-z,A-Z,$,_][a-z,A-Z,0-9,$,_]*
		byte $_$ = 123;
		System.out.println($_$);
		short var3 = 10000; // 2^16
		int var4 = 123; // 2^32
		long var5 = 123; // 2^64
	}

}
