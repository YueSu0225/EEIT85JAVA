package tw.RC.tutor;

public class RC03 {

	public static void main(String[] args) {
		// float, double
		float var1 = 10;
		System.out.println(var1);
		float var2 = 10.1f; // +f是要告訴程式10.1為float
		double var3 = 12.3;
		
		boolean var4 = true; //所有邏輯判斷都是用true & false
		
		char c1 = 'a'; // 表示字元 佔有空間為2^16
		char c2 = 97;  // 列印出來a
		char c3 = '資';
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
	}

}
